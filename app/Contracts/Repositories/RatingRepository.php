<?php

namespace App\Contracts\Repositories;

use App\Contracts\Interfaces\RatingInterface;
use App\Models\ProductTestimonial;

class RatingRepository extends BaseRepository implements RatingInterface
{
    public function __construct(ProductTestimonial $testimonial)
    {
        $this->model = $testimonial;
    }

    /**
     * Handle the Get all data event from models.
     *
     * @return mixed
     */
    public function get(): mixed
    {
        // TODO: Implement get() method.
    }

    /**
     * Handle store data event to models.
     *
     * @param array $data
     *
     * @return mixed
     */
    public function store(array $data): mixed
    {
        return $this->model->query()
            ->create($data);
    }

    /**
     * Handle show method and update data instantly from models.
     *
     * @param mixed $id
     * @param array $data
     *
     * @return mixed
     */
    public function update(mixed $id, array $data): mixed
    {
        return $this->model->query()
            ->findOrFail($id)
            ->update($data);
    }
}
